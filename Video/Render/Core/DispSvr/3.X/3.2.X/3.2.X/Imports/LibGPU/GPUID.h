// handpick mobility radeon out, for some reasons, its id is inserted into rage128's
#define UTIL_PCI_IsRage128(id) (((id>=PCI_DEVICE_ID_R128_BEGIN_1 && id<=PCI_DEVICE_ID_R128_END_1) ||\
	                            (id>=PCI_DEVICE_ID_R128_BEGIN_2 && id<=PCI_DEVICE_ID_R128_END_2)) &&\
								(id!=0x5460))

#define UTIL_PCI_IsRadeon(id)  ((id>=PCI_DEVICE_ID_RADEON_BEGIN_1 && id<=PCI_DEVICE_ID_RADEON_END_1) ||\
	                            (id>=PCI_DEVICE_ID_RADEON_BEGIN_2 && id<=PCI_DEVICE_ID_RADEON_END_2) ||\
								(id>=PCI_DEVICE_ID_RADEON_BEGIN_3 && id<=PCI_DEVICE_ID_RADEON_END_3) ||\
								(id>=PCI_DEVICE_ID_RADEON_BEGIN_4 && id<=PCI_DEVICE_ID_RADEON_END_4) ||\
								(id>=PCI_DEVICE_ID_RADEON_BEGIN_5 && id<=PCI_DEVICE_ID_RADEON_END_5))

#define UTIL_PCI_IsAtiLegacy(id) ((id>=PCI_DEVICE_ID_ATI_LEGACY_BEGIN_1 && id<=PCI_DEVICE_ID_ATI_LEGACY_END_1) ||\
	                              (id>=PCI_DEVICE_ID_ATI_LEGACY_BEGIN_2 && id<=PCI_DEVICE_ID_ATI_LEGACY_END_2) ||\
								  (id>=PCI_DEVICE_ID_ATI_LEGACY_BEGIN_3 && id<=PCI_DEVICE_ID_ATI_LEGACY_END_3) ||\
								  (id>=PCI_DEVICE_ID_ATI_LEGACY_BEGIN_4 && id<=PCI_DEVICE_ID_ATI_LEGACY_END_4) ||\
								  id==PCI_DEVICE_ID_MACH32 || id==PCI_DEVICE_ID_ATI_IGP320M)

#define UTIL_PCI_IsATIX1200SERIES(id) ((id == PCI_DEVICE_ID_ATI_RS690_791E) || (id == PCI_DEVICE_ID_ATI_RS690_791F) ||\
                                       (id == PCI_DEVICE_ID_ATI_RS600_793E) || (id == PCI_DEVICE_ID_ATI_RS600_7941) ||\
                                       (id == PCI_DEVICE_ID_ATI_RS600_7942))

#define UTIL_PCI_IsAtiM10(id) (id>=0x4e50 && 0x4e54>=id)

#define UTIL_PCI_NotRadeon(id)	(UTIL_PCI_IsRage128(id)||UTIL_PCI_IsAtiLegacy(id))

